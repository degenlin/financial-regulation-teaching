def set_bg(slide, color):
    fill = slide.background.fill
    fill.solid()
    fill.fore_color.rgb = color

def add_rect(slide, l, t, w, h, color, alpha=None):
    shape = slide.shapes.add_shape(1, Inches(l), Inches(t), Inches(w), Inches(h))
    shape.fill.solid()
    shape.fill.fore_color.rgb = color
    shape.line.fill.background()
    return shape

def add_text(slide, text, l, t, w, h, size=24, bold=False, color=COLOR_WHITE,
             align=PP_ALIGN.LEFT, wrap=True):
    txBox = slide.shapes.add_textbox(Inches(l), Inches(t), Inches(w), Inches(h))
    tf = txBox.text_frame
    tf.word_wrap = wrap
    p = tf.paragraphs[0]
    p.alignment = align
    run = p.add_run()
    run.text = text
    run.font.size = Pt(size)
    run.font.bold = bold
    run.font.color.rgb = color
    return txBox

def add_bullet_slide(prs, title, bullets, chapter_num, accent=COLOR_LIGHT_BLUE):
    "「」通用内容页「」"
    slide = prs.slides.add_slide(prs.slide_layouts[6])
    set_bg(slide, COLOR_WHITE)

    # 顶部色带
    add_rect(slide, 0, 0, 13.33, 1.4, COLOR_DARK_BLUE)
    add_rect(slide, 0, 1.4, 0.12, 6.1, accent)

    # 章节标签
    add_text(slide, f"第{chapter_num}章", 0.2, 0.1, 2, 0.5, size=11,
             color=COLOR_GOLD, bold=True)
    # 标题
    add_text(slide, title, 0.2, 0.45, 12.5, 0.85, size=26, bold=True,
             color=COLOR_WHITE, align=PP_ALIGN.LEFT)

    # 内容区
    y = 1.65
    for i, item in enumerate(bullets):
        if isinstance(item, tuple):
            level, text = item
        else:
            level, text = 0, item

        indent = 0.35 + level * 0.4
        dot_color = accent if level == 0 else COLOR_DARK_GRAY
        marker = "◆" if level == 0 else "▸"
        marker_color = dot_color

        add_text(slide, marker, indent, y, 0.4, 0.42,
                 size=13 if level == 0 else 11,
                 color=marker_color, bold=(level == 0))
        add_text(slide, text, indent + 0.35, y, 12.0 - indent, 0.42,
                 size=13 if level == 0 else 11,
                 color=COLOR_DARK_BLUE if level == 0 else COLOR_DARK_GRAY,
                 bold=(level == 0), align=PP_ALIGN.LEFT)
        y += 0.46 if level == 0 else 0.40

    return slide

