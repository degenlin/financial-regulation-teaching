def generate_ppt(chapter):
    prs = Presentation()
    prs.slide_width = Inches(13.33)
    prs.slide_height = Inches(7.5)

    num   = chapter["num"]
    title = chapter["title"]

    # 1. 封面
    make_cover(prs, num, title,
               chapter["subtitle"], chapter["description"])

    # 2. 目录
    all_section_titles = [s[0] for s in chapter["sections"]]
    add_bullet_slide(prs, "本章内容框架",
                     [(0, t) for t in all_section_titles],
                     num, accent=COLOR_GOLD)

    # 3. 学习目标
    make_objectives(prs, num, chapter["objectives"])

    # 4. 各节内容
    for sec_title, bullets in chapter["sections"]:
        add_bullet_slide(prs, sec_title, bullets, num)

    # 5. 案例分析
    make_case(prs, num, chapter["case_title"],
              chapter["case_content"], chapter["case_analysis"])

    # 6. 本章小结
    make_summary(prs, num, title, chapter["summary"])

    # 7. 思考题
    make_questions(prs, num, chapter["questions"])

    out_path = os.path.join(OUTPUT_DIR, chapter["file"])
    prs.save(out_path)
    print(f"  ✓ 已生成: {chapter['file']}（{len(prs.slides)} 页）")


